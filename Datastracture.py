# List datastructure

my_list=["Kenya","Nijeria","Uganda","Tanzania","Brazil","Congo"]
my_list.sort()
print(my_list)
my_list[2]="South Africa"
print(my_list[2])
print("I am from", my_list[0])
print(f"I am from {my_list[0]}")

# tuple datastructure
